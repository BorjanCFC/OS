# Да се напише командна процедура што ќе провери дали првиот аргумент од командна
# линија е 1, 2 или 3 и ќе испечати соодветни пораки. Доколку не е ниту една од овие
# вредности да испечати порака за грешка. Задачата да се реши со if и case структури.

#!/bin/bash

if [ $1 -eq 1 ] 
then
echo "Eden"
elif [ $1 -eq 2 ] 
then
echo "Dva" 
elif [ $1 -eq 3 ] 
then
echo "Tri"
else
echo "Greska"
fi